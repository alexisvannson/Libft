int	ft_isalpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(((*str >= 'A') && (*str <= 'Z')) || 
              ((*str >= 'a') && (*str <= 'z'))))
				return (0);
		i++;
	}
	return (1);
}